int	ft_sqrt(int nb)
{
	long long	x;
	long long	N;

	N = (long long)nb;
	x = 0;
	while (1)
	{
		if (x * x == N)
			return ((int)x);
		if (x * x > N)
			return ((int)0);
		x++;
	}
}
